    public void Retuen_Load(){
        int c;
        try{
            pst=con.prepareStatement("select * from r");
            rs=pst.executeQuery();
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v2=new Vector();
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString("ID"));                    
                    v2.add(rs.getString("MemberId"));
                    v2.add(rs.getString("Book"));
                    v2.add(rs.getString("RDateEx"));
                    v2.add(rs.getString("BookR"));
                    v2.add(rs.getString("DateOver"));
                }
                d.addRow(v2);
            }
        }catch(SQLException ex){
            Logger.getLogger(Retuen.class.getName()).log(Level.SEVERE,null,ex);
        }
    }

