    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String i = txtid.getText();
        String b = txtbook.getSelectedItem().toString();
        String r = txtr.getText();
        String r1 = txtr1.getText();
        String d = txtd.getSelectedItem().toString();
        
try {
    pst = con.prepareStatement("insert into r (MemberId,Book, RDateEx,BookR,DateOver) values (?, ?, ?,?,?)");
    pst.setString(1, i);
    pst.setString(2, b);
    pst.setString(3, r);
    pst.setString(4, r1);
    pst.setString(5, d);
    int k = pst.executeUpdate();

    if (k == 1) {
        JOptionPane.showMessageDialog(this, "Return Book Created");
        txtid.setText("");
        txtbook.setSelectedIndex(-1);
        txtr.setText("");
        txtr1.setText("");
        txtd.setSelectedIndex(-1);
        txtid.requestFocus();
        Retuen_Load();
    } else {
        JOptionPane.showMessageDialog(this, "Error");
    }
} catch (SQLException ex) {
    Logger.getLogger(Retuen.class.getName()).log(Level.SEVERE, null, ex);
}

    }//GEN-LAST:event_jButton1ActionPerformed

