    private void fetchMemberName(String memberId) {
    try {
        pst = con.prepareStatement("SELECT Name FROM member WHERE ID = ?");
        pst.setString(1, memberId);
        rs = pst.executeQuery();

        if (rs.next()) {
            String memberName = rs.getString("Name");
            txtname.setText(memberName);
        } else {
            txtname.setText(""); // Clear the name field if no member with the given ID is found
            JOptionPane.showMessageDialog(this, "Member not found with the entered ID");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Issue.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
    
