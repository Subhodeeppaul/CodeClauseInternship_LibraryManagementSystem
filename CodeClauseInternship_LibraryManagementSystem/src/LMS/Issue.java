    public void Issue_Load(){
        int c;
        try{
            pst=con.prepareStatement("select * from issue");
            rs=pst.executeQuery();
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v2=new Vector();
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString("ID"));
                    v2.add(rs.getString("Name"));
                    v2.add(rs.getString("MemberId"));
                    v2.add(rs.getString("IssueDate"));
                    v2.add(rs.getString("ReturnDate"));
                    v2.add(rs.getString("Book"));
                }
                d.addRow(v2);
            }
        }catch(SQLException ex){
            Logger.getLogger(Issue.class.getName()).log(Level.SEVERE,null,ex);
        }
    }

